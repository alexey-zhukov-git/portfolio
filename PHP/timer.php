<?php

// Выводим сообщение о запуске таймера
echo "Таймер запущен. Программа завершится через один час или нажмите Ctrl+C для ручного завершения.\n";

$seconds = 0; // Начальное значение секунд
$one_hour = 3600; // Один час в секундах

while ($seconds < $one_hour) {
    echo "\033[2J\033[;H"; // Очистка экрана терминала
    echo "Прошедшее время: " . gmdate("H:i:s", $seconds) . "\n"; // Отображаем прошедшее время
    sleep(1); // Ждем 1 секунду
    $seconds++; // Увеличиваем счетчик секунд
}

echo "Программа завершается. Ваше сообщение здесь!\n";
